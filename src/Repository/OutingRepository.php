class OutingRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Outing::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Outing $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Outing $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }



    /**
      * @return Outing[] Returns an array of Outing objects
     */

    public function findByFilterOuting($criteria, $user)
    {
        $outings = null;
//        $criteria['isOrganizer']
        if ($criteria->getIsOrganizer()){
            $outings =  $this->createQueryBuilder('o')
                ->Where('o.campus = :campus')
                ->andWhere('o.organizer = :user')
                ->setParameters(['campus'=> $criteria->getCampus(),'user' => $user])
                ->getQuery()
                ->getResult();
        }
        return $outings
        ;
    }




